public class FireStation {
    private int m1;
    private int m2;
    private int m3;
    private int id;
    private int r1;
    private int r2;
    private int r3;

    public FireStation() {
        m1 = m2 = m3 = 0;
        id = 0;
        r1 =  r2 =  r3 = 0;
    }

    public int getM1() {
        return m1;
    }

    public void setM1(int m1) {
        this.m1 = m1;
    }
    public int getM2() {
        return m2;
    }

    public void setM2(int m2) {
        this.m2 = m2;
    }
    public int getM3() {
        return m3;
    }

    public void setM3(int m3) {
        this.m3 = m3;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    public int getR1() {
        return r1;
    }

    public void setR1(int r1) {
        this.r1 = r1;
    }
    public int getR2() {
        return r2;
    }

    public void setR2(int r2) {
        this.r2 = r2;
    }
    public int getR3() {
        return r3;
    }

    public void setR3(int r3) {
        this.r3 = r3;
    }
    

}
