package core.struct;

/**
 *
 * @author kam
 */
public class Point {

    private double x;
    private double y;

    public Point() {
        x = y = 0;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
